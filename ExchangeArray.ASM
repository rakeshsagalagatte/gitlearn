;THIS IS THE PROGRAM OF EXCHANGING THE DATA OF ONE ARRAY TO THE ANOTHER ARRAY

ORG 00H
    SJMP 30H
ORG 30H
     ; GIVE THE SIZE OF AN ARRAY TO THE REG.5
	 ; GIVE THE SBA TO R0 AND DBA TO R1
	 ; ENTER THE A[0] VALUE IN REG.4  AND IT TAKES THE VALUE SBA 
	        MOV A,R5
			MOV R6,A
			MOV 31H,R5
VALUE:		MOV @R0,31H
	 		INC R0
	 		INC R4
	 		DJNZ R5,VALUE
			MOV A,R1
			ADD A,R6
			MOV A,R6
			MOV R5,A
BOTTOMTOUP:	DEC R1
            DEC R0
            MOV A,@R0
			XCH A,@R1
			MOV @R0,A
			DJNZ R6,BOTTOMTOUP
LOOP:       SJMP LOOP
END
            
			

	 